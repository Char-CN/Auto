package org.blazer.dataload.model;

public class SQLParser {

	private String placeholder;

	private int minusLength;

	private int start;

	private int end;

	public String getPlaceholder() {
		return placeholder;
	}

	public void setPlaceholder(String placeholder) {
		this.placeholder = placeholder;
	}

	public int getMinusLength() {
		return minusLength;
	}

	public void setMinusLength(int minusLength) {
		this.minusLength = minusLength;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

}
