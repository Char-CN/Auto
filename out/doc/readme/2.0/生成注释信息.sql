
select TABLE_NAME,COLUMN_NAME,COLUMN_COMMENT from (
select TABLE_NAME,COLUMN_NAME,COLUMN_COMMENT,ORDINAL_POSITION 
from `COLUMNS`
where TABLE_SCHEMA = 'DW_MetaData'
UNION all
select TABLE_NAME,'' as COLUMN_NAME,
TABLE_COMMENT as COLUMN_COMMENT,
0 as ORDINAL_POSITION 
from `TABLES`
where TABLE_SCHEMA = 'DW_MetaData'
) tb
order by TABLE_NAME,ORDINAL_POSITION
;
